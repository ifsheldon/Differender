def in_circles(i, y=0.7, dist=2.5):
    x = math.cos(i) * dist
    z = math.sin(i) * dist
    return torch.tensor([x, y, z], dtype=torch.float32)


def get_rand_pos(bs=None, dist=2.7):
    if bs is None:
        return F.normalize(torch.randn(3), dim=0) * dist
    else:
        return F.normalize(torch.randn(bs, 3), dim=1) * dist
